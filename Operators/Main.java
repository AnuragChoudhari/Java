package Operators;

// Types of operators in Java
// 1) Arithmetic Operators 
// 2) Assignment Operators -> =, +=, etc 
// 3) Comparison Operators -> ==, >=, <=
// 4) Logical Operators -> &&, ||, !
// 5) Bitwise Operators  -> &, ! (Operates bitwise) 

public class Main {
    public static void main(String[] args) {
        int a = 2;
        int b = 3;
    
        // a = 1 0 
        // b = 1 1
        // --------
    // a & b = 1 0 

        System.out.println(a|b);
    }
}
